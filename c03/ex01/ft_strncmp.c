int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (!n)
		return (0);
	while (s1[i] && s2[i] & n > i && s1[i] == s2[i])
	{
		i++;
	}
	if (n > i)
		return (s1[i] - s2[i]);
	else
		return (0);
}
